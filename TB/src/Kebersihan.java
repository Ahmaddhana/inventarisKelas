
public class Kebersihan {

	private String SirkulUdara;
	private double NilaiCahaya, Kelembapan, Suhu;
	
	public String getSirkulUdara() {
		return SirkulUdara;
	}
	public void setSirkulUdara(String sirkulUdara) {
		SirkulUdara = sirkulUdara;
	}
	public double getNilaiCahaya() {
		return NilaiCahaya;
	}
	public void setNilaiCahaya(double nilaiCahaya) {
		NilaiCahaya = nilaiCahaya;
	}
	public double getKelembapan() {
		return Kelembapan;
	}
	public void setKelembapan(double kelembapan) {
		Kelembapan = kelembapan;
	}
	public double getSuhu() {
		return Suhu;
	}
	public void setSuhu(double suhu) {
		Suhu = suhu;
	}
}
